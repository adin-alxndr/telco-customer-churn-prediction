# !pip install kagglehub (uncomment if kagglehub is not installed yet)

import pandas as pd
import numpy as np
import kagglehub

import matplotlib.pyplot as plt
import seaborn as sns

from sklearn.model_selection import train_test_split, cross_val_score
from sklearn.preprocessing import OneHotEncoder, StandardScaler
from sklearn.compose import ColumnTransformer
from sklearn.pipeline import Pipeline
from sklearn.metrics import accuracy_score, classification_report, confusion_matrix, roc_auc_score, roc_curve
from sklearn.linear_model import LogisticRegression
from sklearn.ensemble import RandomForestClassifier

"""### 3. LOAD DATA"""

path = kagglehub.dataset_download("blastchar/telco-customer-churn")
print("Dataset path:", path)

df = pd.read_csv(path + "/WA_Fn-UseC_-Telco-Customer-Churn.csv")
df.head()

"""### 4. DATA UNDERSTANDING"""

df.shape

df.info()

df.describe()

df['Churn'].value_counts(normalize=True)

"""### 5. DATA CLEANING"""

# Convert TotalCharges to numeric
df['TotalCharges'] = pd.to_numeric(df['TotalCharges'], errors='coerce')

# Check missing values
df.isnull().sum()

# Fill missing values with median
df['TotalCharges'].fillna(df['TotalCharges'].median(), inplace=True)

# Drop customerID (not useful for prediction)
df.drop('customerID', axis=1, inplace=True)

"""### 6. EXPLORATORY DATA ANALYSIS (EDA)"""

sns.countplot(x='Churn', data=df)
plt.title("Churn Distribution")
plt.show()

sns.heatmap(df.select_dtypes(include='number').corr(), annot=True, cmap='coolwarm')
plt.title("Correlation Matrix")
plt.show()

sns.boxplot(x='Churn', y='MonthlyCharges', data=df)
plt.title("Monthly Charges vs Churn")
plt.show()

sns.countplot(x='Contract', hue='Churn', data=df)
plt.title("Contract Type vs Churn")
plt.show()

sns.countplot(x='InternetService', hue='Churn', data=df)
plt.title("Internet Service vs Churn")
plt.show()

"""### 7. FEATURE ENGINEERING & PREPROCESSING"""

X = df.drop('Churn', axis=1)
y = df['Churn'].map({'Yes':1, 'No':0})

cat_features = X.select_dtypes(include='object').columns
num_features = X.select_dtypes(exclude='object').columns

preprocessor = ColumnTransformer(
    transformers=[
        ('cat', OneHotEncoder(handle_unknown='ignore'), cat_features),
        ('num', StandardScaler(), num_features)
    ]
)

df.head()

"""### 8. TRAIN TEST SPLIT"""

X_train, X_test, y_train, y_test = train_test_split(
    X, y, test_size=0.2, stratify=y, random_state=42
)

"""### 9. MODEL PIPELINES

**Logistic Regression**
"""

log_model = Pipeline(steps=[
    ('preprocessor', preprocessor),
    ('classifier', LogisticRegression(
        max_iter=3000,
        class_weight='balanced',
        solver='lbfgs'
    ))
])

"""**Random Forest**"""

rf_model = Pipeline(steps=[
    ('preprocessor', preprocessor),
    ('classifier', RandomForestClassifier(
        n_estimators=300,
        random_state=42,
        class_weight='balanced'
    ))
])

"""### 10. CROSS VALIDATION & TRAIN MODELS

**Logistic Regression**
"""

log_cv = cross_val_score(log_model, X_train, y_train, cv=5, scoring='roc_auc')

print("Logistic Regression CV AUC:", log_cv.mean())

"""**Random Forest**"""

rf_cv = cross_val_score(rf_model, X_train, y_train, cv=5, scoring='roc_auc')

print("Random Forest CV AUC:", rf_cv.mean())

"""**Train Models**"""

log_model.fit(X_train, y_train)
rf_model.fit(X_train, y_train)

y_pred_log = log_model.predict(X_test)
y_pred_rf = rf_model.predict(X_test)

"""### 11. Evaluation"""

print("Logistic Regression")
print(classification_report(y_test, y_pred_log))

print("Random Forest")
print(classification_report(y_test, y_pred_rf))

"""### 12. CONFUSION MATRIX"""

cm = confusion_matrix(y_test, y_pred_rf)

sns.heatmap(cm, annot=True, fmt='d', cmap='Blues')
plt.xlabel("Predicted")
plt.ylabel("Actual")
plt.title("Confusion Matrix - Random Forest")
plt.show()

"""### 13. ROC CURVE"""

y_prob = rf_model.predict_proba(X_test)[:,1]
roc_auc = roc_auc_score(y_test, y_prob)

fpr, tpr, _ = roc_curve(y_test, y_prob)

plt.plot(fpr, tpr, label=f"AUC = {roc_auc:.2f}")
plt.plot([0,1],[0,1],'--')
plt.xlabel("False Positive Rate")
plt.ylabel("True Positive Rate")
plt.title("ROC Curve")
plt.legend()
plt.show()

"""### 14. FEATURE IMPORTANCE"""

ohe = rf_model.named_steps['preprocessor'].named_transformers_['cat']
feature_names = list(ohe.get_feature_names_out(cat_features)) + list(num_features)

importances = rf_model.named_steps['classifier'].feature_importances_

feat_imp = pd.Series(importances, index=feature_names).sort_values(ascending=False)

feat_imp.head(10).plot(kind='barh')
plt.title("Top 10 Important Features")
plt.show()

feat_imp.head(10).plot(kind='barh')
plt.title("Top 10 Important Features")
plt.show()

